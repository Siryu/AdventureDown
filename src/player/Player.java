package player;

public class Player
{
	private int gold;
	private int fame;
	private String name;
	
	public int getGold()
	{
		return gold;
	}
	public void setGold(int gold)
	{
		this.gold = gold;
	}
	public int getFame()
	{
		return fame;
	}
	public void setFame(int fame)
	{
		this.fame = fame;
	}
	public String getName()
	{
		return name;
	}
	public void setName(String name)
	{
		this.name = name;
	}
}
